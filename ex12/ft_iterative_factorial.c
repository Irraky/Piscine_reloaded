int		ft_iterative_factorial(int nb)
{
	int i;

	i = nb;
	nb = 1;
	if (i < 0 || i > 12)
		return (0);
	if (i == 0)
		return (1);
	while (i != 0)
	{
		nb = nb * i;
		--i;
	}
	return (nb);
}
