int		ft_count_if(char **tab, int (*f)(char*))
{
	int compteur;
	int i;

	compteur = 0;
	i = 0;
	while (tab[i] != '\0')
	{
		if ((*f)(tab[i]) == 1)
			++compteur;
		++i;
	}
	return (compteur);
}
